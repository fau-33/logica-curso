"""
    2. Calculando a Área de um Círculo
Crie um programa que calcule a área de um círculo com base no raio fornecido pelo
usuário.
● Fórmula: Área = π * raio²
● Use π = 3.14159
"""

raio = float(input("Digite o raio do círculo: "));
pi = 3.14159;
area = pi * raio**2;
print("A area do círculo é: " , area);